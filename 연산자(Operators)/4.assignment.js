// 할당 연산자 (Assignment Operators)
let a = 1;
a = a + 2;
console.log(a); // 3

// 산수 연산자와 병행해서 쓰이는 할당 연산자
a += 2; // a = a + 2;를 축약한 버전이다. 
console.log(a); // 5

a -= 2; // a = a - 2;
console.log(a); // 3

a *= 2; // a = a * 2;
console.log(a); // 6

a /= 2; // a = a / 2;
console.log(a); // 3

a %= 2; // a = a % 2; 나머지 연산자
console.log(a); // 1

a **= 2; // a = a ** 2; 제곱 연산자
console.log(a); // 1